// Función para abrir el modal
function openFooterModal(type) {
    const modal = document.getElementById('footerModal');
    const modalTitle = document.getElementById('footerModalTitle');
    const modalBody = document.getElementById('footerModalBody');
    
    if (footerModalContent[type]) {
        modalTitle.textContent = footerModalContent[type].title;
        modalBody.innerHTML = footerModalContent[type].content;
        modal.style.display = 'block';
        document.body.style.overflow = 'hidden'; // Prevenir scroll del body
    }
}

// Función para cerrar el modal
function closeFooterModal() {
    const modal = document.getElementById('footerModal');
    modal.style.display = 'none';
    document.body.style.overflow = ''; // Restaurar scroll del body
}

// Event listeners cuando el DOM esté listo
document.addEventListener('DOMContentLoaded', function() {
    // Cerrar modal al hacer clic en la X
    const closeBtn = document.querySelector('.footer-modal-close');
    if (closeBtn) {
        closeBtn.addEventListener('click', closeFooterModal);
    }
    
    // Cerrar modal al hacer clic fuera del contenido
    const modal = document.getElementById('footerModal');
    if (modal) {
        modal.addEventListener('click', function(event) {
            if (event.target === modal) {
                closeFooterModal();
            }
        });
    }
    
    // Cerrar modal con la tecla ESC
    document.addEventListener('keydown', function(event) {
        if (event.key === 'Escape') {
            closeFooterModal();
        }
    });
});
